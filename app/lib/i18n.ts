export function t(key: string, lang: string, params?: Record<string, string>): string {
  const entry = translations[key];
  if (!entry) return key;
  let text = entry[lang] || entry["en"] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, v);
    }
  }
  return text;
}

